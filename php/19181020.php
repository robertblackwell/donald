<h2>No. 3 Stationary Hospital,
No 1 Marque,
France,
20th October 1918</h2>

<p>Dear Nellie</p>

<p>In hospital with ‘flu’, getting on a fairly well. Have been down four days, now. Will you please write home. Too weak to do any writing. If Neita saw a letter like this she would you go silly. No hope of getting to Blighty.</p>

<p>Your loving cousin</p>
<p>Don</p>

