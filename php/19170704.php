<h2>Baby  photo postcard, France, 4th July 1917</h2>

<p>My darling wife</p>

<p>Here are 4 postcards which I think will suit you, I bought them while on leave the other day. Posted you a long letter last night giving all the news. Think these cards are very good. Love and kisses to my dear wife from your loving husband</p>

<p>Don</p>

