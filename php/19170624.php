<p>Amiens - La Gare du Nord, France</p>

<p>Postcard</p>

<p>My darling wife</p>

<p>I wrote you a couple of days ago, nothing of importance has happened since then. Hope those five cards  are welcomed. The building in which we are billeted was a military hospital, our quarters being Number 4 ward. On or about today June 24 is I understand the time for the great event. Wonder what you darling are doing at the present moment 2:35 PM. Sincerely hope and trust that everything will be well. It might have happened a week ago or it may be another week before it takes place so dear every minute I am expecting the cable giving me the joyous news. I answered Aunty Fonâ€™s long letter the other day. Hope to be spending another day at the city during the week. It is grand to get away from the military for a few hours. Will write a letter in a day or so when I hope to have some news of little Mac.</p>

<p>Love and kisses to my darling wife from your ever own and loving husband.</p>

