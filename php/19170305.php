<h2>Lark Hill, 5th March 1917</h2>

<p>Postcard</p>

<p>My dearest wife</p>

<p>How is this photo. This photo was taken in front of the post office at Durban, the nigger boy allowed himself to be included after a great deal of persuasion. He is a newsboy. This is the party that did the ‘la grand” while on leave in Durban. Here are their names Robertson, Forsythe, Cook, your hubby, Allison, Crowley, King, Mc Harq. This is an enlargement on a pocket camera. Your loving husband.</p>

<p>Don</p>

<p>My dear wife</p>

<p>Another photo. Taken at the sports ground, Cape Town from the grandstand. Tug of war championship final being pulled, won by  field artillery. Yours truly is somewhere in that crowd with back to the camera but cannot be picked out. You have a good look and see if you can pick out your old man. Your loving husband.</p>

<p>Don</p>

