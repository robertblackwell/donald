<h2>28th of January 1917</h2>

<p>Postcard “under the spreading chestnut tree” The Smithy at Figheldean, Salisbury Plain.</p>

<p>My dearest wife</p>

<p>This is where I went yesterday afternoon and had a very good time. Five of us had photos taken under the tree. Will send you one kiddie as soon as I get them. The tree at present is bare not a leaf on it.</p>

<p>Love Don</p>

