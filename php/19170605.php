<h2>France, 5th June 1917</h2>

<p>POSTCARD </p>

<p>My darling wife</p>

<p>Since arriving in our present position we have had plenty of work to do in fact the hardest work I have done  since being in uniform so therefore have had very little time to write. No letters since arriving in France but now that I have settled down they should be putting in an appearance. The town we were billeted in we left last Friday at 9:30 AM a short march indeed, joining up with our division at 10 AM. Our division has been doing some very heavy fighting and are now out for a few weeks well earned rest. Soon as we reach the camp we were ……</p>

