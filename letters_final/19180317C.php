<p>Just a couple of postcards  of Heytesbury. This is the church which I attended last Sunday. It's a fine old place all English churches are very much alike. The other card is the main street in appearance very much like the French villages. Notice the building with the sign over the doorway “Red Lion Inn”. This is the hotel I use to visit when I first came into this district. Many a good tea I had at this place. A Mrs Randall keeps the hotel I knew her son very well and prior to his going to France he and I used to spend  Sunday afternoons and evenings together. 
</p>
<p>Passed through the doctors this morning, OK, he was a little doubtful. Going before the dentist in the morning why aren't you and Bubs here, then we could go for a bonsa walk. I’d carry Bubs, bet your life. Never mind the time may not be far distant and I'm home with my two darlings. Wrote a number of letters yesterday including one to your dear self. There's one thing in going to France now, I've missed the worst of the winter. Have had a good spin in England.</p>
<p>Au revoir for the present. Love and kisses to my darling wife and bonny sun from your ever own loving hubby and daddy.  Don
</p>

