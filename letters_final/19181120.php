<p>I am sorry to have been so long in answering your letter asking for details of your cousins death, Donald C McDonald. He was too ill all along for us ever to talk to him and get any messages. He was delirious and unconscious most of the time but the end was peaceful and quiet. He is buried in the English cemetery he in Rouen. Each grave is marked by a cross on which is the soldiers name and Regiment. The sister in charge of the ward wrote full particulars to his people in New South Wales. We have had such a terrible time and lost so many from this epidemic of influenza the pneumonia.</p>
<p>Yours faithfully</p>
<p>G. I. Laudely</p>
<p>Assistant Matron</p>
