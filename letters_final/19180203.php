<div class='closing'>
<p>I wrote your mummy I long letter last night. Shortly after writing it along came a beautiful cake and a postcard from Madge. Will you give mum a big hug and kiss for me. The cake was beautiful and fresh, nearly finished it at supper last night. Talk about supper, this is what I had, tomato soup, salmon, some tongue, then a dish of OXO, two pieces of toast one piece of bread and cheese and finish with three slices of cake. Not a bad feed, think it was an attempt to see who could eat the most, anyhow it was a dead heat. Well talking to your uncle George this morning he is grand and going strong. Had a letter from Auntie Jane this morning. </p>
<p>My word, my boy, mummy has been telling me you have a &ldquo;Paddy&rdquo; and kick up a terrible noise, of course I don't believe her, anyhow you are boss, so do as you like and see that you get everything you want. Next time you are doing a cake walk on mummy&rsquo;s  &hellip;.. stamp and kick like the very &lsquo;duce&rsquo; and don't forget an extra  one for me. Hope you are a real good boy, Tons of kisses and oceans of love to you and mummy.</p>
<p>From your own loving Daddy</p>
