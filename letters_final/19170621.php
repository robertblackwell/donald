<p>Here are five postcards which I am sure will be of interest to you. Yesterday morning we broke camp and moved off to a French village about 10 km distance. We are billeted fairly comfortable. There is one good feature about this village we are able to purchase strawberries, cherries and oranges that is while the money lasts. The country is looking grand at present, flowers growing wild everywhere, the poppy being the most prominent. The whole countryside is covered with poppies. These cards are places where I have visited. Anxiously waiting for the cable kiddie. Au revoir for the present Love and kisses to my darling wife from your ever own and loving husband.</p>
