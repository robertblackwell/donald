<p>Just back from four days holiday in London. Had a great time. I am dog tired. Will tell you all the news tomorrow night. This is the &ldquo;Benalla &ldquo; in Dakar Harbour on Christmas Day 1916.</p>

<p style="font-weight:bold">this communcation presumably arrived on a post card, or a letter containing a picture, which is now missing</p>

<p>Love and kisses from your loving husband</p>
<p>Don</p>
<p>Who is this says you, well darling it&rsquo;s supposed to be your hubby<span class='fn'>I assume this letter contained the sketch mentioned and that is what this sentence refers to. That sketch is missing</span>. Sketched for 2p by an old man on the Thames Embankment on Sunday morning 11th of January 1917.  Do you think it's like me, Kiddie? If you could have only seen me posing for it and dozens of people looking on. Never forget it. Had a mad fit on, I think.</p>
<p>Writing tomorrow all particulars of my trip to the big smoke, London. Had a great time in the old historic city. </p>
<p>To my darling little wife.</p>
<p>Your loving husband</p>
<p>Don</p>
