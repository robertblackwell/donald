<div class="closing">
<p>Just a line kiddie,  &rdquo;Waiting for the train to Southampton&rdquo; then on to France. It will certainly be days before I have the chance of writing again. Wrote you a number of letters lately, one every day. Hope you receive them. Received a Christmas gift this morning tobacco, lollies, sugar, handkerchieves from the Comfort Gard. A card was inside indicating that it was sent by Doris Backland, Hunters Hill Sydney. May drop her a line acknowledging this gift. It's a request of the Comfort Gard to acknowledge the gifts. Had a letter from Nellie last night, Bernie has been staying there for 6 days. He is grand although worries a little about his leg. She also received a note from George. Arrived France okay.
Au revoir for the present darling. Love kisses and embraces to my darling wife and precious son with oceans of love and kisses galore from your very own loving husband and father.</p>
<p>Don</p>
