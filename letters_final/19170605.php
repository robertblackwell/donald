<p>Since arriving in our present position we have had plenty of work to do, in fact the hardest work I've done since being in uniform so therefore had very little time to write. No letters since arriving in France but now that I've settled down they should be putting in an appearance. The town we were billeted in we left last Friday at 9:30,  a very short march indeed, joining up with our division at 10 am. Our division has been doing some very heavy fighting and are now out for a few weeks well earned rest. As soon as we reached the camp we were attached to the second section, 2nd D.A.C. and then allotted huts. Much to my surprise Hilton Hain of East Maitland is in the same hut, of course greetings were flying round. I have a very comfortable bed, wire stretched across two beams, then straw, a waterproof blanket, can assure you it's okay, but sweetheart it's nothing to be compared to ‘ours’, what say you kiddie. </p>
<p>The days are very hot but in the latter part of the night it gets rather cold. The other night we could plainly hear the heavy bombardments but we are well out of the way. On Sunday Hilton Hain and I went over to the battery camp about five minutes walk, to have a yarn to Billy Foster. What a time we had between the three of us, we each gained a  great amount of news. Heard a lot of Maitland Society talk, it's true that one has to come away from home to hear news. Bill produced a parcel, he had just received from Tom Scobie, biscuits to be sure, and they got nothing bet your life. Today we were reviewed by General Birdwood. Extremely hot day. Darling news is scarce, there's a lot I could write about but of course no mention must be made concerning military movements.</p> <p>You people must all be disgusted with Russia.</p>
<p>Kiddie note paper is very scarce, so have to resort to postcards until we move into some decent sort of town. Well darling how are you getting on. Sincerely hope and trust that everything is okay. By the time you receive this, what oh, mum and dad, some class then kiddie. Time is getting close now and then for a cable giving me the glad news. Will give you my new military address. You can address letters either to Nellie Fry or new address just as you please.</p>

