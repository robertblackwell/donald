<p>I posted you a letter yesterday but as there is another mail tomorrow I will write a few lines. They is no news but a  line or 2 won&rsquo;t go amiss will they kiddie.</p>

<p>I started off calling you my little wife but I suppose you are getting bigger now  like a whale.</p>

<p>There is a lot of talk about a number of signalers going to a school in another camp right down in the south. Sincerely hope I am one to be sent, it will only be a case of luck who is sent. We are all waiting for the names to come out 25 of our men are to get their disembarkation leave next week and waiting now for the officer to come in and take our names. Four out of 6 men in our hut are to get leave so I stand a good chance. It is bitterly cold today unbearable, snow still lying about. Walking down the YMCA  steps tonight slipped on the ice came a cropper but did not get hurt.</p>

<p>We are getting starved, there&rsquo;s sure to be trouble if things don&rsquo;t improve. Anyhow I had a good tea; peas, potatoes and sausages for the sum of 1/2. Food is getting very scarce in England. Kiddie tell them at home I could not possibly write to them by this mail, too busy so if there is anything to tell you can do so sweetheart. What about doing some washing for me, have plenty to do, can't get time to do it. Not like the Warrens cannot throw it over the fence here and get my wife to do it anyhow you will have plenty of washing to do when little Mac comes along, what do you say darling. Every afternoon I attend a riding school, have to have 14 lessons to complete the course. In my next letter I hope to be able to write all about my four days leave. Sincerely hope you are in the best of health, how is the little darling getting on sweetheart. Be sure and tell me all. Remember me to all. Love and kisses to my darling wife from your most loving and sincere husband,  Don</p>

<p>Catch these dear XXXXXXXXXXXXXXXXXXXX</p>
<div class='closing'>
<p>This is Wednesday morning. After I got to bed last night received a Mercury, evening news posted by mum needless to say I read every word. It was so grand to get the old paper. No doubt you'll receive this later about 18 March so will now wish you many happy returns of the day. As soon as I go on leave will buy something for my dear and post same straight away. I'll be home for your next birthday kiddie also for little Mac&rsquo;s too. We will have a good bust up on that day kiddie.
My hands are like ice it's absolutely the coldest day since we arrived. </p>

<p>Love and kisses to my darling wife. </p>

<p>Your loving husband.</p>

<p>Don</p>
