<p>This book seeks to preserve, and make accessible, the letters that Donald Cameron McDonald wrote to his new young wife Neita during the period 1916, 1917 and 1918, and to expound a little more on Donald's expeience and death in the great war.</p>

<p>I am Nina Marguerite Blackwell (nee McDonald), the author or compiler of this book, and the eldest of three 
grand child (all daughters) of Donald and Neita, and eldest daughter of Frank Neville Cameron McDonald the only child of Donald and Neita.</p>

<p>For most of my life I knew that my grandfather had died in the Great War and that my paternal grand mother
had raised her only child (my father) by her self. But that story was history and like children and young adults everywhere
my focus was on my own life and looking forward.</p>

<p>In recent years as I have become elderly I have begun to acquire an interest in the story of my fathers parents,
an interest that has been facilitated by the fact that many of the letters and postcards Donald send to Neita during those war years 
remain in the families possession. But of course I now regret the opportunity I missed by not delving into this 
topic while Neita and others of her generation where alive.</p>

<p>As each of the Grandchildren have aged and decided to downsize their homes Donald's letters and cards have been moved several times. On the last move the three grand daughters decided that all three should examine all document remaining from our grandparents and parents.</p>

<p>Examination of Donald's correspondence revealed that the documents, mostly written in ink but some in pencil, would you not last much longer. Many are after all over 100 years old, and others nearly soon be. So as the eldest granddaughter I took on the task of 
digitalising them, before they were lost forever.</p> 

<p>Whoever that turned out to be a sizeable task for I quickly realized that I wanted to make these documents into some form 
of book and that for such a purpose simply scanning or photocopying the originals would not be sufficient. Thus I set about
the arduous task of reading all the documents into a speach recognition program in order to get actual text documents.
This highly error prone task required many hours of proof reading and corrections in addition to the initial reading aloud of all 
the documents.  I also scanned all documents as a backup and as the only way to preserve images such as the photos on 
the face of postcards.</p>

<p>At the end of this process I had the right raw material from which to start the process of making this book. However 
the process was not perfect. Don’s writing was very neat and legible, but age has made the documents difficult to read. I have done my best to record exactly what he wrote with the help of other eyes and magnifying glasses but sometimes the turn of phrase or word has eluded me. Where places were mentioned but spelling difficult to decipher I have checked the validity where possible.</p>

<p>In addition these documents are from the society of 100 years ago so language usage has changed as well as many other things
that Don and Neita took in their stride but that may seem strange to generations younger than mine. I have tried to overcome
these issues with footnotes and appendices to explain; expressions, word usage, places, contemporary historical events
and even contemporary money (pounds shillings and pence).</p>

<p>The documents are personal communications in a family context and as a consequence many people are mentioned only by their first name. Presumably Donald and Neita know well who they are talking about and such people are family or acquaintences. But even my sisters and I could not work out who all of them were. Thus to help readers from younger generations or non family members
we have included a list of the commonly mentioned characters (that we can identify) in an appendix.</p>

