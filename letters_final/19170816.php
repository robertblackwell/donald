<p>Another surprise, I was wounded last Tuesday afternoon (14th August). Two slight wounds in the back, nothing serious. Going under the x-ray to locate one piece of shell. Fritz has been giving us I terrible time, was partially buried twice and gassed once on the one day so consider myself extremely lucky in getting out of it so lightly. My one hope now is Blighty would that not be grand. Will write to you again soon. Going to cable Neita tomorrow. The military are sure to notify her but their message will be vague. Sincerely hope and trust that after all these long months to be able to see you all.</p>
<p>Yours very sincerely</p>
<p>Don</p>


