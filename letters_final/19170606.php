<p>It is with deepest regret that I write expressing my sincere sympathy to your dear, mother, father and family also Vi in the loss you have all sustained in Frank being killed in action on April 4th.
I first heard this sad news from Nelly Fry about a fortnight ago, did not mention anything in my letters until I received some definite information. Nellie said she was not certain if it was true so I wrote straight away to Australian headquarters and to his O.C. asking for particulars. Up to the present no word have I received from them. Sid Fry told Hilton Hain that it was true and yesterday I saw Frank&rsquo;s name in the &ldquo;Anzac&rdquo; paper list. The same old trouble appeared in connection with his name, the letter B was omitted sincerely hope there is a mistake. I will do everything in my power to get any information which might interest you, have already asked several of the boys who have gone up to the particular locality where he was killed to try and get some information.
Kiddie words fail me to express my feelings for you. It must have been a great shock but I&rsquo;m sure you bare the shock equally as well, as you did on the previous occasion. Can easily understand what a trying time you would have had especially as you had more than yourself to consider.</p>
<p>It is a great shame that Frank could not have pulled through but darling you can rest assured he died a hero's death fighting with the finest army in the field &ldquo;The Australians&rdquo;.</p>
<div class='closing'>
<p>Express my greatest sympathy to mother and father and Vi and you dear kiddie accept your husband&rsquo;s sincere sympathy. Keep your heart up dear one.</p>
<p>From your ever own and loving husband with kisses.</p>
<p>Don</p>
