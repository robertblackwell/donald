<div class='closing'>
<p>Thanks very much for your letter dated 6th January which I received last Thursday. Yesterday I received a parcel from Neita which included your cigars. Thanks very much indeed they are &ldquo;tray bong&rdquo;, not one broken and at present I'm smoking one and thoroughly enjoying it. Neita has told me all about your holiday at Toronto what a great time you had, lucky people. What about letting Neita, Bubs and myself have the cottage next Easter, of course we might invite you down, there is a chance. The photos of Bubs and yourself are very good, you certainly do look to be having a good time, what about changing places. Very sorry regarding the conscription issue. The Irish over this side were up against it, why? goodness only knows. They would like to see England fall, they don't consider the consequences. I am returning to France next week sometime. This is a rotten camp, glad in one way to get out of it. Bernie is getting on fine, and he has been down at &ldquo;Fairmead&rdquo; lately. Have not heard anything of George since going overseas. Hope yourself and all at Wadhurst are keeping in best of health. Love and kisses to dear Bubs and Neita.</p>
<!-- --------------------------------------------------------------------------->
<p>Your affectionate son-in-law</p>
<p>Don</p>
