<p>Here are a few postcards of places I visited while on my way from St Pol Hospital (France I believe) to my battery. Very little news to tell you. Leaving here tomorrow, earlier than I expected, for an Australian hospital nearer London I believe. Yesterday had my photo taken out in the grounds, will send along in due course. </p>

