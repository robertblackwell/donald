<div class='keep-together'>

<p>The grounds of the hospital were beautiful no need what so ever to go out. Here it is different, soon as I get away the better. My wounds are healed up well although I may have to go under the x-ray for the one in the back last Thursday a lady from St Albans, Mrs Bates called to see me. She represents the Australian Comforts Committee. Supplied me with tobacco and papers. A large batch are leaving here today for Australia, wish I were going with them, eh darling.</p>

<?php \render_postcard("170907-P-3-fixed"); ?>
</div>
<div class='keep-together'>

<p>We are situated 15 miles from London. A whole days leave is granted once a week and we are allowed out every afternoon from 1 PM p.m. to 8:15 PM. There are some very fine walks about here. Wish you and our darling boy were here, what a good time we could have, eh darling. Soon as I leave here I'm going to have 14 days leave then for a good time, why are you not over here sweetheart. We'll go straight and see Nellie Fry. Of course will call on……..</p>

