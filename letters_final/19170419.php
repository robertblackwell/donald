<p>Your letter of 4 February 1917 found  me last Friday and I was of course pleased to hear from you again, any old news from home is always appreciated. We have been here almost 7 weeks now, but expect to be moved at any time now and as a matter of fact there is a healthy rumour to the effect that we move on Monday next. So far we have had an awfully decent time much better than anticipated and it is bothering the officers, what to do with us, to fill the time. We are never up before 8 o'clock and then we would not get out only breakfast is brought down. I have made the breakfast call my reveille.</p> 
<p>On Sunday I went over to Bulford to meet Don, we went for a stroll and had an afternoon tea together. Don told me he would be going to Salisbury on Saturday. Jim Kerr, wishing to see Don is going to Salisbury on Saturday also and he has arranged for Bob Kerr to be there, so I will try to get to Salisbury also, it is almost 2 years since I have seen Bob, he and I were pals for years. George wrote me last week and it appears by the tone of his letter to be okay. </p>
<div class='closing'>
<p>Neita you know how much I care for writing, so will now bring the business to a close. </p>
<p>Best of luck. Keith</p>
