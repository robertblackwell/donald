# What is it that we know about this man 

We know that he was born on November 4th 1888 in Maitland NSW. His father was born in Australia of Scotish parents and his mother was of Welsh extraction. He attended maitland Boys High School and progressed from there to an accountant's position with a large Neewcastle wholesale firm.

He was a tall outgoing man, six feet in height, and he described himself in one of his letters as being 12 stone 3 pounds "those rounded shoulders have gone and I am as tough as a tack".

From his letters home we consider him to be somewhat conservative in outlook yet prepared like most Australians to buck authority on occassion which he did at times with great delight.

He was beyond doubt a gentle man and with all the hopes and fears that beset men who are called on to leave their loved ones and face an uncertain future.

His letters refer longingly to the times in the future when he and his wife and son would be together again. He long to see and cradle his son but this was not to be and one can only share his anquish late in October 1918 so near to the conclusion of the war, when it no doubt dawned on him that all his hopes and asperations of a joyful reunion and a long happy life would be forever denied him.

It is a sad story, but of course, only ne of many more.

Some five miles or so on the outskirts of the French City of Rouen is the Imperial War Graves Cemetery of St Sever where the bodies of so many British and Commonwealth soldiers lie.

Above the grave of one such soldier stands a grave stonbe which reads :

	30280 DRIVER 
