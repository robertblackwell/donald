LETTER:-"Dear Fred. We are near Amiens. I cannot describe how we are suffering and certainly cannot write Neita along these lines. Have had a touch of gas, eyes and chest, but only a few days off and now while there is a lull I can write to you. I am definitly not well but then many others are worse and we are still coonsidered ablebodied. It is beyond words to describe the constant shelling and all we do is dig and dig and as one shelter is destroyed we dig another. Then we have the constant movement of the guns as they get stuck in the mud, we use the horses that are left and of course push and shove ourselves. The horses are exhausted and frequently if not hit by shrapnel will just fall down and die. Their terror is awfull. They stop, bolt around crazed. It is hell on earth. The dug outs are frequentl half full of water and whilst in the line it is impossible to change clothes and for days on end we are soaked and covered with mud and have to sleep like that as well. From my dugout on this lovely clear day with hardly a cloud in the sky I can see several dead horses that helped pull our gun carriage. They are covered by rats eating the flesh and surrounded by millions of flies. they will just be left there to rot. the stench is nearly unbearable. There are several of our boys lying out there as well that have died during the day. We cannot go out and get them until it is dark. For what it is worth we have advanced about 5 miles and many prisoners are being walked to the rear. They all look shatttered and defeated I guess we must look like them as well for we are a dispirited lot. Still we are advancing so lets hope something will come of it."


