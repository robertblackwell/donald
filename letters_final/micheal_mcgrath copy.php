LETTER:-"Must not forget to tell you about Boulogne and the "Bullring". Whilst we were at Boulonge the town was out of bounds. But being so that is probably the reason a bunch of us decided to see the town. Had a good look. The French very happy to see us. Hard to walk through on the cobbled streets especially as we were carrying full pack and rifle. Didn't see an MP at any time. 

Now for the "Bullring". This was where we were tortured by a pack of British and Australian drill instructors. They did let up day and night for 17 days. Day and night they marched us or ran us and we undergo awful exercises with full battle kit up to knees in mud and slush. They were a sadistic lot and we hated everyone of them. We are told that a previous draft go so mad they put one instructor in hospital. There was a hell of a to-do. Anyway we had to do all this on bully and biscuit. We are starving. There is averse doing the rounds that goes like this:-

	"Somewhere in France soldiers are seen
	Walking about all very lean 
	Walking about with bowed down head 
	Praying to god they will be fed.

	Somewhere in France in mud and rain 
	Soldiers are saying 'never again'
	Never again in one deep breath
	will they come to France and starve to death "

Well we finally survived the running and marching and then odd hun aeropplane which came over and dropped a bomb on the parade ground which did no explode."



