# ANOTHER MAN'S WAR

<p>Recently I had the priviledge to read a diary and some 75 year old letters from a soldier from the First World War
commencing with his departure from Sydney in November 1916 on S. S. Benalla. These letters were mainly to his young pregnant wife of only a few months and embody the highs and lows of this young man with his lofty expectations of a great adventure and travel and finishes in the filth, degredation, and misery of the trenches in Octaber 1918.</p>

<p>I suppose if placed in similar circumstances as this man was most young me would surely seize an opportunity to travel 
and taste adventure.</p>

<p>These notes in diary form and occassionally with extracts from letters redord the events and at times his thoughts and yearnings
for those two years. Because of censorship requirements he was unable and did not indicate precisely where some of these 
military actions took place but most were on the Somme where geat battles took place over the war years.</p>

