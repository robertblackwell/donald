<p>Work work; plenty of it, so please excuse only a short note. Something doing tomorrow which means more work. Have been doing exceedingly well for letters, every day I&rsquo;ve received some. Today only a few minutes ago, two came along, one from Neita, contained a number of photos of my big son, they are just lovely. Also received another paper three or four days ago. 
Had a terrible storm last night, the boys were out in it all, &lsquo;its&rsquo; only a rumour that they got wet. I'm okay. Hope you are the same.
Love to self and all at are Fairmead.</p>
<p>Your loving brother</p>
<p>Don</p>
