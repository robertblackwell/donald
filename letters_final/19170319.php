<p>Have absolutely no news. I wrote you a letter yesterday for the mail which closes today. There is another mail tomorrow so therefore thought I would send one by that mail also, don't suppose you mind kiddie how often you receive letters.</p>

<p>Up to the present, we signalers had been scattered all over the camp, but today saw a  change, we are all together from now on and our conditions of training etc will be greatly improved. The greatest improvement will be no duty at weekends whatever. This  practically gives us free weekends, we have to be in camp on Sunday morning for 9 o'clock parade. </p>

<p>Had a talk to Mr. Craven this afternoon, he said Radcliffe camp has got worse and worse. The son cleared out then the fence was nailed up. This did not matter as conditions in camp were very much better they were able to go in and out just as they liked. Radcliff&rsquo;s was full, mostly Queensland  soldiers and their wives. </p>

<p>We hear recruits are not being taken for the artillery in New South Wales and all artillery camps are going to be closed down. Wrote to Frank and George last week but of course no reply yet.</p>

<p>Today saw our food cut down again, the boys are getting very restless, talking of going on strike. This will surely happen. Dripping was issued instead of margarine today for the second time. Although the food supply is being cut down I am not doing too badly at all. </p>

<p>More good news came through from France today, got the Germans running. No doubt all you people at home are overjoyed. The men in camp take it as a matter of course. Another batch of our drivers are off to the front tonight. </p>

<p>Have not received any letters for over a week. Suppose they will come in a batch. By the time you receive this letter sweetheart you won't have far to go to the great event. How interesting you must be now kiddie, getting bigger and bigger every day, what would I give to drop down beside you at this very moment, but never mind, time will soon pass, I sincerely hope, then home to my loving little wife and our little baby. What a time we will have then. I'm always planning what we will do and where  we will spend a holiday. I'll have my work cut out then looking after two, two of the best absolutely. Sweetheart I'll now say au revoir. </p>
<div class='closing'>
<p>Going to drop a line to Mrs. Smith. </p>
<p>Hoping that the war will soon end so that I can return to &ldquo;my own&rdquo; with tons and tons of kisses to my darling wife from your ever own devoted and loving husband.</p>
<p>Don  XXXXXXXXXX</p>
