<p>After posting your letter yesterday, kiddie, I received the card informing me that there was a registered letter at the post office for me, so straight up to the post office. What was it, why the cable from your darling, of birthday greetings. Nellie also had a letter enclosed. Fancy, kiddie getting it right on the day, simply grand. Wanted to fly straight away to Australia to my darlings but I could not find wings strong enough, so take the kisses right now sweetheart. XXXXXX XXXXXXXXXXXXXXXXXXX</p>

<p>Last night did not go to the pictures as anticipated, but went to a song service at the YMCA. Another young chap and myself went together, and while there we had a contest as to who could sing the loudest. Well we both made plenty of noise, but came to no decision so settled it afterwards in the hut. Ended in a draw. </p>

<p>Nellie&rsquo;s letter was very bright, as usual, told me that her father was ill under the &ldquo;Dr&rdquo; but was on the improve. Also said that Fritz had been over but had not dropped any bombs near Goudhurst. This morning I received two Sydney Mails from Phyll. Very nice to receive them, there was a general rush by the boys in the hut to see them. One photo took my eye, Newcastle on French Day. Some of the pictures taken from the front are very funny, would like to know where some of them were taken. Nellie sent me a bosker*
	<span class="fn">bosker, Australian slang meaning first rate or excellent</span>
parcel of cakes and butter today. Home-made butter, plenty of toast tonight for supper. </p>

<p>Well I told you that leave was stopped on Saturday thus preventing my going into Salisbury, it was lifted today, just my luck.</p>

<p>The weather the last four days has been very mild but again today it&rsquo;s bitterly cold. Will have to get a pair of leather gloves straight away. Nellie said she had a letter from George, who was very well and thought I'd be passing through the camp in which he was. Hope I come across him.</p>

<div class='closing'>

<p>There is no news today sweetheart so I'll ring off with love, kisses and embraces to my darling wife and bonny son from your ever own and loving husband and father with tons and tons of love and kisses my two darlings.</p>

<p>Hubby and daddy</p>

<p>Don</p>

