<p>This is the hospital ship I came over from France on. She was painted iron grey and travelled as a transport, not as a hospital ship, for it is considered far safer for transports then hospital ships. Well darling I now say au revoir, will send some more postcards in a day or so. Suppose our dear boy is getting to be a fair scamp, look after him, kiddie till I get back, then I'll take a turn in looking after him and giving him a jolly good time. Love kisses and embraces to my darling wife and dear son from your ever loving husband with the millions and millions of kisses.</p>
