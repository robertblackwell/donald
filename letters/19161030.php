<div class="letter">
	<h2>Tuesday 30th October 1916 (I estimate.)</h2>

	<p>Dear mother</p>

	<p>Postcard</p>

	<p>Just received word sailing by the P&amp;O Lansport Benalla Thursday week the 9th (November). In all probability will be home for the weekend though not certain yet, will advise later. Neita is okay. Hope to see you on the weekend. </p>
	<p>Your affectionate son-in-law.</p>
	<p>Don</p>

