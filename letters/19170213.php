<h2>Lark Hill Camp, 13th of February 1917</h2>

<p>My dearest wife</p>

<p>Just back from four days holiday in London. Had a great time. I am dog tired. Will tell you all the news tomorrow night. This is the “Benalla “ in Dakar Harbour on Christmas Day 1916.</p>

<p>Love and kisses from your loving husband</p>

<p>Don</p>

<p>Who’ this says you, well darling it’s supposed to be your hubby. Sketched for 2p by an old man on the Thames Embankment on Sunday morning 11th of January 1917.  Do you think it's like me, Kiddie? If you could have only seen me posing for it and dozens of people looking on. Never forget it. Had a mad fit on, I think.</p>

<p>Writing tomorrow all particulars of my trip to the big smoke, London. Had a great time in the old historic city. </p>

<p>To my darling little wife.</p>

<p>Your loving husband</p>

<pre><code>Don
