<h2>November 1918</h2>

<p>Dear Neita</p>

<p>Your birthday cable arrived a week before. I also sent that out to him. How he loved your letters. You kept him posted with full details and he loved the photos of yourself and baby. My prayers are for you and I feel just awful as if I had lost my darling brother.</p>

<p>Nellie</p>

<p>Love to mother and all</p>
<p>Everything seems so hopeless today. Wished I had cable you when he was ill, it would have prepared you a little.</p>
<p>Thanks for the enclosed slips. The prayer lovely.</p>
<p>We use the other in our church.</p>
<p>Did you like the song? God send you back to me. I mean the words</p>

<p>Love from all </p>

<p>Nellie</p>

